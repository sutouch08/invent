<h1>เข้าระบบ</h1>
<p>
1. เปิด web browser (แนะนำให้ใช้ firefox หรือ chrome ) แล้วพิมพ์ชื่อเครื่อง หรือ IP address ของเครื่องที่ทำหน้าที่เป็นแม่ข่าย สมมุติว่าเครื่องแม่ข่ายชื่อ SERVER ให้พิมพ์ตามภาพตัวอย่าง <br/>
<img class="pic" src="images/invent/browser_logo.png" /><br />
<img class="pic" src="images/invent/1.0.1.png" />
<br/>
ระบบจะนำท่านเข้าสู่หน้า Login เพื่อลงชื่อเข้าใช้ระบบ ให้ท่านใส่อีเมล์ หรือ ชื่อผู้ใช้ ตามที่ได้รับจากผู้ดูแลระบบ ในช่อง Email OR User Name แล้วใส่รหัสผ่านในช่อง Password จากนั้นคลิก Sign in หรือ กดปุ่ม Enter 
