<h1>เพิ่ม/แก้ไข พนักงานขาย</h1>
<p>
พนักงานขายจะแตกต่างจากพนักงานทั่วไป คือ พนักงานขายจะมียอดขาย ซึ่งยอดขายของพนักงานขายจะถูกเชื่อมโยงกับลูกค้า ยอดซื้อที่มาจากลูกค้าที่พนักงานขายรับคนใดรับผิดชอบก็จะเป็นยอดขายของพนักงานขายคนนั้น
และพนักงานขายจะถูกแบ่งออกตาม <a href="index.php?content=customer_group">กลุ่มของลูกค้า</a> หรือ ที่เรียกว่า <a href="index.php?content=customer_group">เขตการขาย</a> เช่น นาย ก เป็นพนักงานขาย ภาคกลาง เป็นต้น การเพิ่มพนักงานขาย มีขั้นตอนดั้งนี้
