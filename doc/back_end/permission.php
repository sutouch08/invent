<h1>กำหนดสิทธิ์</h1>
<p>
การกำหนดสิทธิ์ เป็นการจำกัดการเข้าถึงข้อมูล หรือ จำกัดการใช้งานของผู้ใช้งาน เพื่อป้องกันไม่ให้ผู้ที่ไม่เกี่ยวข้องหรือผู้ที่ไม่รู้ เข้าไปสร้างความเสียหายกับข้อมูลในระบบ เพราะฉนั้นผู้ดูแลระบบจำเป็นต้องกำหนดสิทธิ์การใช้งานของ ผู้ใช้งานแต่ละคนให้เหมาะสมกับหน้าที่ความรับผิดชอบ เพื่อป้องกันความเสียหายที่อาจเกิดจากความรู้เท่าไม่ถึงการณ์ของผู้ใช้งานระบบ เช่น พนักงานคลังสินค้าเผลอไปลบข้อมูลสินค้าโดยไม่ได้ตั้งใจ จะส่งผลให้ ไม่สามารถขายสินค้าตัวนั้นได้ เป็นต้น ขั้นตอนการกำหนดสิทธิ์ มีดังนี้
</p>
