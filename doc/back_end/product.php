<h2>เพิ่มรายการสินค้า</h2>
<p>
การเพิ่มรายการสินค้า มี 2 วิธี คือ เพิ่มทีละรายการ และ การใช้ตัวสร้างรายการอัตโนมัติ แล้วกลับมาแก้ไขรายละเอียด
</p>
