<h1>เพิ่ม/แก้ไข สินค้า</h1>
<p>
เริ่มแรก ก่อนที่จะเพิ่มสินค้า เราต้องทำความเข้าใจเกี่ยวกับ สินค้า ในระบบเสียก่อน ใน SI สินค้าจะถูกมองเป็น 2 แบบ คือ รุ่นสินค้า หรือ กลุ่มสินค้า และ รายการสินค้า หรือ สินค้าย่อย <br />
<strong style="color:red">รุ่นสินค้า</strong>&nbsp; คือต้นแบบของตัวสินค้า ซึ่งในรุ่นสินค้านั้นๆ จะมี สินค้าอยู่ภายในรุ่นซึ่งอาจแยกตามคุณลักษณะของตัวสินค้า เช่น สินค้ารุ่นนี้มี 2 สี 2 ไซด์ แสดงว่าสินค้ารุ่นนี้ มี 4 ตัวสินค้าที่แตกต่างอยู่ภายใน ยกตัวอย่างเช่น สินค้า  A มีสี แดง และ ดำ มีไซด์ M และ L แสดงว่าสินค้ารุ่นนี้จะมี สินค้า A สีแดง ไซด์ M , A สีแดง ไซด์ L, A สีดำ ไซด์ M, A สีดำ ไซด์ L รวม 4 รายการสินค้า<br />
<strong style="color:red">รายการสินค้า</strong>&nbsp; คือรายการสินค้าที่มีคุณลักษณะอย่างน้อยหนึ่งคุณลักษณะ(ซึ่งใน กลุ่มสินค้าหรือรุ่นสินค้า จะไม่มีคุณลักษณะเลย) เมื่อเราเพิ่มรุ่นสินค้า เราจะต้องเพิ่มรายการสินค้านี้เข้าไปด้วยเสมออย่างน้อย 1 รายการ
</p>
<p>
ขั้นตอนการเพิ่มสินค้าจะเริ่มจาก  เพิ่มรุ่นสินค้าและรายละเอียดสินค้า -> เพิ่มรูปภาพสินค้า -> เพิ่มรายการสินค้า  ซึ่งวิธีการมีดังนี้
</p>
