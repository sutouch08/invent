<p>
3. กำหนดรหัสอ้างอิงสินค้ารายการนี้ ในรูปตัวอย่าง กำหนดจาก รหัสรุ่น(KFB-S082)-รหัสสี(AL)-รหัสไซด์(M) ซึ่งรหัสอ้างอิงนี้ไม่สามารถกำหนดซ้ำกันได้<br />
4. เลืกกำนหดสีให้ตรงกับสินค้า (ถ้ามี)<br />
5. เลืกกำนหดไซด์ให้ตรงกับสินค้า (ถ้ามี)<br />
6. เลืกกำนหดคุณลักษณะให้ตรงกับสินค้า (ถ้ามี)<br />
7. กำหนดบาร์โค้ดให้สินค้า (ถ้ามี)<br />
8. กำหนดบาร์โค้ดของแพ็คสินค้า (ถ้ามี)<br />
9. ใส่จำนวนในแพ็คสินค้า (ถ้ามี)<br />
10. ใส่ราคาทุนของสินค้าโดยราคาทุนเริ่มต้นจะถูกดึงมาจากราคาที่เราใส่ไว้ตอนเพิ่มรุ่นสินค้า หากราคาไม่เท่ากันให้เรากำหนดใหม่ (บางกรณีต่างไซด์ราคาอาจต่างกัน) หากไม่มีราคาทุนให้กำหนดเป็น 0.00<br />
11. ใส่ราคาขายของสินค้าโดยราคาขายเริ่มต้นจะถูกดึงมาจากราคาที่เราใส่ไว้ตอนเพิ่มรุ่นสินค้า หากราคาไม่เท่ากันให้เรากำหนดใหม่ (บางกรณีต่างไซด์ราคาอาจต่างกัน) หากไม่มีราคาขายให้กำหนดเป็น 0.00<br />
12 - 15  ใส่น้ำหนัก, ความกว้าง, ความยาว, ความสูง ของสินค้า โดยค่าเริ่มต้นคือค่าที่เรากำหนดไว้ตอนเพิ่มรุ่นสินค้า หากต่างกันให้เรากำหนดใหม่ (ต่างไซด์น้ำหนัก, ความกว้าง, ความยาว, ความสูง ย่อมต่างกัน๗ หากไม่มีให้กำหนดเป็น 0.00<br />
16. เลือกรูปภาพให้ตรงกับสินค้า<br />
17. บันทึก<br />
ทำซ้ำขั้นตอนที่ 3 - 17 จนครบทึกรายการสินค้าในรุ่นนี้<br />
</p>
